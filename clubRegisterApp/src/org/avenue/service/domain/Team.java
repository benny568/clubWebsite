package org.avenue.service.domain;

public class Team {

	private int id;
	private String name;
	private int lrcode;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLrcode() {
		return lrcode;
	}
	public void setLrcode(int lrcode) {
		this.lrcode = lrcode;
	}	
}
